from fastapi import FastAPI
from fastapi.responses import FileResponse
import uvicorn

PATH = 'app/templates'

app = FastAPI()

@app.get("/")
async def root():
    return FileResponse(f'{PATH}/index.html')

if __name__ == "__main__":
    uvicorn.run(
        "main:app",
        host='127.0.0.1',
        port=8000,
        reload=True
    )
